--USING ROWNUM AND SUBQUERY

SELECT ID,FIRST_NAME,LAST_NAME,START_DATE,END_DATE,SALARY,CITY,DESCRIPTION
FROM
  (SELECT E.*,ROWNUM RN FROM EMPLE E
  )
WHERE RN =4;


SELECT ID,FIRST_NAME,LAST_NAME,START_DATE,END_DATE,SALARY,CITY,DESCRIPTION FROM (
   SELECT E.*,ROWNUM RN FROM EMPLE E WHERE ROWNUM < 5 )
WHERE  RN = 4;

--USING ROWNUM AND GROUP BY HAVING CLASUSE

SELECT ID,FIRST_NAME,LAST_NAME,START_DATE,END_DATE,SALARY,CITY,DESCRIPTION FROM EMPLE A
GROUP BY rownum,ID,FIRST_NAME,LAST_NAME,START_DATE,END_DATE,SALARY,CITY,DESCRIPTION HAVING rownum = 4;


--USING ROWID AND COUNT() AGGREGATION FUNCTION

SELECT * FROM EMPLE A
WHERE 4 =(SELECT COUNT(ROWID) FROM EMPLE B WHERE A.ROWID > = B.ROWID);

--USING COUNT() ANALYTICAL FUNCTION 
--LOGIC IS USE ORDER BY ROWNUM. IN REALTIME IT IS GOOD TO AVOID THIS SOLUTION

SELECT ID,FIRST_NAME,LAST_NAME,START_DATE,END_DATE,SALARY,CITY,DESCRIPTION FROM 
(
SELECT E.*,COUNT(ROWNUM) OVER(ORDER BY ROWNUM )RN FROM EMPLE E
)WHERE RN=4;


--USING WITH AS

WITH RNEMP AS
(
SELECT E.*,ROWNUM RN FROM EMPLE E
)
SELECT E1.* FROM EMPLE E1, RNEMP R
WHERE RN=4
AND E1.ID=R.ID;

--GENERATE ROWNUM WITHOUT USING ROWNUM PSEUDO COLUMN AND ROW_NUMBER() ANALYTICAL FUNCTION 

SELECT E.*,(SELECT COUNT(ID) FROM EMPLE E1 WHERE E1.ID <= E.ID) FROM EMPLE E;

